import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";
import {
  DEFAULT_EXCLUDE,
  DEFAULT_INCLUDE,
  DEFAULT_SOURCE_ROOT,
  resolveCorpusRoot,
} from "../lib/corpus.ts";
import { loadV2TaskSuite } from "../lib/tasks.ts";

const PLACEHOLDER_COMMIT = "__RUN_SETUP_CORPUS__";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");
const evalsDir = path.resolve(repoRoot, "packages/evals");
const tasksDir = path.resolve(evalsDir, "tasks");
const outputPath = path.resolve(evalsDir, "promptfooconfig.v2.yaml");
const taskSuite = loadV2TaskSuite(tasksDir);
const expectedCommit = readPinnedCommit(resolveCorpusRoot(repoRoot)) ?? PLACEHOLDER_COMMIT;

const providerConfig = {
  corpusRoot: relativeFromProviders(path.resolve(repoRoot, DEFAULT_SOURCE_ROOT)),
  expectedCommit,
  include: DEFAULT_INCLUDE,
  exclude: DEFAULT_EXCLUDE,
};

const config = {
  description: "Budge eval v2 - Next.js corpus",
  providers: [
    {
      id: "file://providers/budge-v2.ts",
      label: "budge",
      config: {
        ...providerConfig,
        orchestratorModel: "openai/gpt-5.4-mini",
        workerModel: "openai/gpt-5.4-mini",
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
      },
    },
    {
      id: "file://providers/rag-v2.ts",
      label: "rag (bm25)",
      config: {
        ...providerConfig,
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
        topK: 20,
      },
    },
    {
      id: "file://providers/monolithic-v2.ts",
      label: "monolithic agent",
      config: {
        ...providerConfig,
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
        maxSteps: 30,
      },
    },
  ],
  prompts: ["{{payload}}"],
  tests: [
    ...taskSuite.synthesis.tasks.map((task) => ({
      description: summarizeTask(task.task, "Synthesis"),
      vars: { payload: task.task },
      assert: task.assert,
      metadata: { category: taskSuite.synthesis.category, taskType: "task" },
    })),
    ...taskSuite.lookup.tasks.map((task) => ({
      description: summarizeTask(task.task, "Lookup"),
      vars: { payload: task.task },
      assert: task.assert,
      metadata: { category: taskSuite.lookup.category, taskType: "task" },
    })),
    ...taskSuite.chat.chatScenarios.map((scenario) => ({
      description: `Chat: ${scenario.name}`,
      vars: {
        payload: JSON.stringify({ name: scenario.name, turns: scenario.turns }),
      },
      assert: [
        {
          type: "llm-rubric",
          value: `Across the full transcript, the answers stay grounded in the same subsystem, respond to the follow-up questions directly, and remain source-specific rather than generic framework explanations for the scenario "${scenario.name}".`,
        },
      ],
      metadata: {
        category: taskSuite.chat.category,
        taskType: "chat",
        scenarioName: scenario.name,
      },
    })),
  ],
  outputPath: "./results/v2-latest.json",
  sharing: false,
  evaluateOptions: {
    maxConcurrency: 1,
    timeoutMs: 1200000,
  },
};

const yaml = YAML.stringify(config, { lineWidth: 0 });
fs.writeFileSync(
  outputPath,
  `# yaml-language-server: $schema=https://promptfoo.dev/config-schema.json\n# Generated by scripts/generate-v2-config.ts\n${yaml}`,
);

function readPinnedCommit(corpusRoot: string): string | undefined {
  const marker = path.join(corpusRoot, ".eval-commit");
  if (!fs.existsSync(marker)) return undefined;
  const commit = fs.readFileSync(marker, "utf8").trim();
  return commit.length > 0 ? commit : undefined;
}

function relativeFromProviders(targetPath: string): string {
  const providersDir = path.resolve(evalsDir, "providers");
  return path.relative(providersDir, targetPath).replace(/\\/g, "/");
}

function summarizeTask(task: string, prefix: string): string {
  const trimmed = task.trim();
  if (trimmed.length <= 72) return `${prefix}: ${trimmed}`;
  return `${prefix}: ${trimmed.slice(0, 69)}...`;
}
