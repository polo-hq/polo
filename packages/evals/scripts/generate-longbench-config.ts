import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";
import {
  buildLongBenchPayload,
  DEFAULT_FULL_DUMP_MAX_PROMPT_TOKENS,
  DEFAULT_LONGBENCH_DATA_PATH,
  DEFAULT_SAMPLE_SIZE,
  getLongBenchTaskType,
  type LongBenchTaskType,
  resolveLongBenchDataPath,
  selectLongBenchSubset,
  type LongBenchItem,
} from "../lib/longbench.ts";

const HARD_TASK_TYPE_TARGETS: Record<LongBenchTaskType, number> = {
  "multi-hop-qa": 14,
  "summarization-interpretive": 13,
  "single-hop-qa": 9,
  "structured-code-reasoning": 8,
  "icl-translation": 4,
};

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");
const evalsDir = path.resolve(repoRoot, "packages/evals");
const outputPath = path.resolve(evalsDir, "promptfooconfig.longbench.yaml");
const datasetPath = resolveLongBenchDataPath(
  repoRoot,
  process.env.LONGBENCH_DATA_PATH ?? DEFAULT_LONGBENCH_DATA_PATH,
);
const sampleSize = numberFromEnv(process.env.LONGBENCH_SAMPLE_SIZE, DEFAULT_SAMPLE_SIZE);
const maxFullDumpPromptTokens = numberFromEnv(
  process.env.LONGBENCH_MAX_FULL_DUMP_PROMPT_TOKENS,
  DEFAULT_FULL_DUMP_MAX_PROMPT_TOKENS,
);

if (!fs.existsSync(datasetPath)) {
  throw new Error(
    `Missing LongBench dataset at ${datasetPath}. Download https://huggingface.co/datasets/THUDM/LongBench-v2/resolve/main/data.json first.`,
  );
}

const dataset = JSON.parse(fs.readFileSync(datasetPath, "utf8")) as LongBenchItem[];
const selection = selectLongBenchSubset(dataset, {
  difficulty: ["hard"],
  lengths: ["short", "medium"],
  sampleSize,
  maxFullDumpPromptTokens,
  taskTypeTargets: HARD_TASK_TYPE_TARGETS,
});

const tests = selection.selected.map((item) => ({
  description: summarizeQuestion(item),
  vars: { payload: JSON.stringify(buildLongBenchPayload(item)) },
  assert: [{ type: "icontains", value: item.answer }],
  metadata: {
    _id: item._id,
    answer: item.answer,
    domain: item.domain,
    sub_domain: item.sub_domain,
    task_type: getLongBenchTaskType(item),
    difficulty: item.difficulty,
    length: item.length,
  },
}));

const config = {
  description: `Budge eval - LongBench v2 hard short/medium subset (${tests.length} fit-cap cases)`,
  providers: [
    {
      id: "file://providers/longbench-budge.ts",
      label: "budge",
      config: {
        orchestratorModel: "openai/gpt-5.4-mini",
        workerModel: "openai/gpt-5.4-mini",
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
        maxSteps: 20,
      },
    },
    {
      id: "file://providers/longbench-rag-bm25.ts",
      label: "rag (bm25)",
      config: {
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
        topK: 20,
        chunkSize: 500,
        overlap: 50,
      },
    },
    {
      id: "file://providers/longbench-full-dump.ts",
      label: "full-dump",
      config: {
        actionModel: "openai/gpt-5.4-mini",
        provider: "openrouter",
      },
    },
  ],
  prompts: ["{{payload}}"],
  tests,
  outputPath: "./results/longbench-latest.json",
  sharing: false,
  evaluateOptions: {
    maxConcurrency: 1,
    timeoutMs: 1200000,
  },
};

const header = [
  "# yaml-language-server: $schema=https://promptfoo.dev/config-schema.json",
  "# Generated by scripts/generate-longbench-config.ts",
  `# Dataset: ${path.relative(evalsDir, datasetPath).replace(/\\/g, "/")}`,
  `# Filter: difficulty=hard, length in [short, medium], fit-cap <= ${maxFullDumpPromptTokens} estimated tokens`,
  `# Eligible cases: ${selection.eligible.length}, filtered out by fit cap: ${selection.filteredOutByFitCap.length}, selected: ${selection.selected.length}`,
  "# Sampling: per-difficulty subdomain floor, then fill toward explicit task-type targets",
  `# Hard task-type targets: ${renderTaskTypeTargets(HARD_TASK_TYPE_TARGETS)}`,
  "",
].join("\n");

fs.writeFileSync(outputPath, `${header}${YAML.stringify(config, { lineWidth: 0 })}`);

logCounts("FIT_CAP_FILTERED_TASK_TYPE_COUNTS", selection.filteredOutByFitCap, (item) =>
  getLongBenchTaskType(item),
);
logCounts("DIFFICULTY_COUNTS", selection.selected, (item) => item.difficulty);
logCounts("TASK_TYPE_COUNTS", selection.selected, (item) => getLongBenchTaskType(item));
const domainCounts = countBy(selection.selected, (item) => item.domain);
for (const [domain, count] of domainCounts) {
  console.log(`${domain}: ${count}`);
}
logCounts("SUBDOMAIN_COUNTS", selection.selected, (item) => `${item.domain} / ${item.sub_domain}`);
console.log(
  `Wrote ${path.relative(repoRoot, outputPath)} with ${selection.selected.length} cases.`,
);

function summarizeQuestion(item: LongBenchItem): string {
  const prefix = `${item.domain} / ${item.sub_domain}`;
  const trimmed = item.question.trim().replace(/\s+/g, " ");
  const summary = trimmed.length <= 96 ? trimmed : `${trimmed.slice(0, 93)}...`;
  return `${prefix}: ${summary}`;
}

function countBy<T>(items: T[], keyFn: (item: T) => string): Map<string, number> {
  const counts = new Map<string, number>();
  for (const item of items) {
    const key = keyFn(item);
    counts.set(key, (counts.get(key) ?? 0) + 1);
  }
  return new Map(Array.from(counts.entries()).sort(([left], [right]) => left.localeCompare(right)));
}

function logCounts<T>(label: string, items: T[], keyFn: (item: T) => string): void {
  console.log(label);
  if (items.length === 0) {
    console.log("(none)");
    console.log("");
    return;
  }
  for (const [key, count] of countBy(items, keyFn)) {
    console.log(`${key}: ${count}`);
  }
  console.log("");
}

function numberFromEnv(value: string | undefined, fallback: number): number {
  if (!value) return fallback;
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function renderTaskTypeTargets(targets: Record<LongBenchTaskType, number>): string {
  return Object.entries(targets)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([taskType, count]) => `${taskType}=${count}`)
    .join(", ");
}
